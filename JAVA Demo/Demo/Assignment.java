package com.demo;

public class Assignment
{
	public static void main(String[] args)
	{
		int num1 = 16, num2 = 14;
		float f1 = (float) 3.8, f2 = (float) 8.3;
		
		int addition = num1 + num2;
		int subtraction = num2 - num1;
		float product = f1 * f2;
		
		System.out.println("Addition of two integers: " + addition);
		System.out.println("Subtraction of two integers: " + subtraction);
		System.out.println("Multiplication of two decimal numbers: " + product);		
	}
}
